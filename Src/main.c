void ws2812b_set_pixel(uint32_t pixel, uint8_t r, uint8_t g, uint8_t b,
		uint8_t *buff) {
	uint8_t i;
	uint8_t color;
	i = 0;
	color = g;
	while (i < 8) {
		if (color & 0b1000000) {
			buff[(24 * pixel) + i] = 3;
		} else {
			buff[(24 * pixel) + i] = 1;
		}
		color = color << 1;
		i++;
	}
	color = r;
	while (i < 16) {
		if (color & 0b1000000) {
			buff[(24 * pixel) + i] = 3;
		} else {
			buff[(24 * pixel) + i] = 1;
		}
		color = color << 1;
		i++;
	}
	color = b;
	while (i < 24) {
		if (color & 0b1000000) {
			buff[(24 * pixel) + i] = 3;
		} else {
			buff[(24 * pixel) + i] = 1;
		}
		color = color << 1;
		i++;
	}
}
